public class Operations {

    public float sum(float a, float b){
        return a+b;
    }

    public float sub(float a, float b){
        return a-b;
    }

    public float mult(float a, float b){
        return a*b;
    }

    public float div(float a, float b){
        return a/b;
    }

    public float exp(float a){
        return a*a;
    }

}
